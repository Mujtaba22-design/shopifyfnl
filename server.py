@app.route('/api/generate-pdf', methods=['POST'])
def generate_pdf():
    """Generates a professional PDF audit report for a specific lead"""
    lead = request.json
    buffer = io.BytesIO()
    p = canvas.Canvas(buffer, pagesize=letter)
    
    # PDF Styling
    p.setFont("Helvetica-Bold", 18)
    p.drawString(100, 750, "SHOPIFY LEAD GENIUS - SEO AUDIT")
    
    p.setStrokeColorRGB(0.2, 0.4, 0.8)
    p.line(100, 740, 500, 740)
    
    p.setFont("Helvetica-Bold", 12)
    p.drawString(100, 710, f"Business Name: {lead['name']}")
    p.setFont("Helvetica", 12)
    p.drawString(100, 690, f"Website: {lead['url']}")
    p.drawString(100, 670, f"Contact Email: {lead['email']}")
    
    # SEO Score Section
    p.setFont("Helvetica-Bold", 14)
    p.drawString(100, 630, f"SEO Health Score: {lead['seo_score']}/100")
    
    p.setFont("Helvetica", 10)
    p.drawString(100, 610, "Status: Improvements Recommended")
    
    p.setFont("Helvetica-Oblique", 9)
    p.drawString(100, 100, "Generated by ShopifyLeadGenius - Confidential Audit Report")
    
    p.showPage()
    p.save()
    
    buffer.seek(0)
    return send_file(
        buffer, 
        as_attachment=True, 
        download_name=f"Audit_{lead['name'].replace(' ', '_')}.pdf", 
        mimetype='application/pdf'
    )

