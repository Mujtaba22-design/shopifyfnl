def perform_seo_audit(url):
    """Analyzes the HTML of a store to find common SEO issues"""
    try:
        # Adding a user-agent to avoid being blocked by stores
        headers = {'User-Agent': 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36'}
        response = requests.get(url, timeout=10, headers=headers)
        soup = BeautifulSoup(response.text, 'html.parser')
        
        score = 100
        issues = []
        
        # Check for H1 Header
        if not soup.find('h1'):
            score -= 20
            issues.append("Missing H1 Header (Critical for SEO)")
        
        # Check for Meta Description
        if not soup.find('meta', attrs={'name': 'description'}):
            score -= 20
            issues.append("Missing Meta Description")

        # Check for Image Alt Tags
        images = soup.find_all('img')
        if images and not any(img.get('alt') for img in images):
            score -= 10
            issues.append("Images missing Alt text")
        
        return score, issues
    except Exception as e:
        return 50, [f"Could not reach site: {str(e)}"]

